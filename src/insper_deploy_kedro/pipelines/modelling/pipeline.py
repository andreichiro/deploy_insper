"""Pipeline de modelagem: otimizar 3 modelos -> avaliar -> selecionar melhor -> relatório de teste."""

from typing import Any

from kedro.pipeline import Pipeline, node, pipeline

from insper_deploy_kedro.registry import (
    build_training_run_manifest,
    record_experiment_run,
)

from .nodes import (
    build_bootstrap_metric_intervals,
    build_cv_fold_metrics,
    build_feature_selection_bundle,
    build_model_frontier,
    build_model_selection_scorecard,
    build_modelling_design_audit,
    build_nested_cv_audit,
    build_permutation_feature_importance,
    build_perturbation_sensitivity_audit,
    build_split_comparison_report,
    build_threshold_metrics,
    enrich_best_model_config_with_policy,
    evaluate_all_on_test,
    evaluate_model,
    optimize_model,
    select_best_model,
    select_deployment_policy,
    select_feature_columns,
    summarize_cv_fold_metrics,
    summarize_perturbation_sensitivity_audit,
)


def create_pipeline(**kwargs: Any) -> Pipeline:  # noqa: ARG001
    """Monta o DAG de otimização/avaliação/seleção/relatório."""
    return pipeline(
        [
            node(
                func=select_feature_columns,
                inputs=[
                    "master_table",
                    "params:columns",
                    "params:ml_runtime",
                    "params:feature_selection",
                    "params:baseline",
                ],
                outputs=[
                    "baseline_selected_feature_columns",
                    "baseline_feature_selection_frontier",
                    "baseline_feature_selection_stability",
                    "baseline_feature_selection_manifest",
                ],
                name="select_feature_columns_baseline_node",
                tags=["modelling", "feature_selection"],
            ),
            node(
                func=optimize_model,
                inputs=[
                    "master_table",
                    "baseline_selected_feature_columns",
                    "params:baseline",
                    "params:ml_runtime",
                    "params:decision_policies",
                    "params:model_selection",
                    "params:calibration",
                ],
                outputs="baseline_model",
                name="optimize_baseline_node",
                tags=["modelling", "training"],
            ),
            node(
                func=evaluate_model,
                inputs=[
                    "master_table",
                    "baseline_model",
                    "baseline_selected_feature_columns",
                    "params:evaluation",
                ],
                outputs="baseline_metrics",
                name="evaluate_baseline_node",
                tags=["modelling", "evaluation"],
            ),
            node(
                func=select_feature_columns,
                inputs=[
                    "master_table",
                    "params:columns",
                    "params:ml_runtime",
                    "params:feature_selection",
                    "params:optimization",
                ],
                outputs=[
                    "optimized_selected_feature_columns",
                    "optimized_feature_selection_frontier",
                    "optimized_feature_selection_stability",
                    "optimized_feature_selection_manifest",
                ],
                name="select_feature_columns_optimized_node",
                tags=["modelling", "feature_selection"],
            ),
            node(
                func=optimize_model,
                inputs=[
                    "master_table",
                    "optimized_selected_feature_columns",
                    "params:optimization",
                    "params:ml_runtime",
                    "params:decision_policies",
                    "params:model_selection",
                    "params:calibration",
                ],
                outputs="optimized_model",
                name="optimize_catboost_node",
                tags=["modelling", "training"],
            ),
            node(
                func=evaluate_model,
                inputs=[
                    "master_table",
                    "optimized_model",
                    "optimized_selected_feature_columns",
                    "params:evaluation",
                ],
                outputs="optimized_metrics",
                name="evaluate_optimized_node",
                tags=["modelling", "evaluation"],
            ),
            node(
                func=select_feature_columns,
                inputs=[
                    "master_table",
                    "params:columns",
                    "params:ml_runtime",
                    "params:feature_selection",
                    "params:xgboost",
                ],
                outputs=[
                    "xgboost_selected_feature_columns",
                    "xgboost_feature_selection_frontier",
                    "xgboost_feature_selection_stability",
                    "xgboost_feature_selection_manifest",
                ],
                name="select_feature_columns_xgboost_node",
                tags=["modelling", "feature_selection"],
            ),
            node(
                func=optimize_model,
                inputs=[
                    "master_table",
                    "xgboost_selected_feature_columns",
                    "params:xgboost",
                    "params:ml_runtime",
                    "params:decision_policies",
                    "params:model_selection",
                    "params:calibration",
                ],
                outputs="xgboost_model",
                name="optimize_xgboost_node",
                tags=["modelling", "training"],
            ),
            node(
                func=evaluate_model,
                inputs=[
                    "master_table",
                    "xgboost_model",
                    "xgboost_selected_feature_columns",
                    "params:evaluation",
                ],
                outputs="xgboost_metrics",
                name="evaluate_xgboost_node",
                tags=["modelling", "evaluation"],
            ),
            node(
                func=build_model_selection_scorecard,
                inputs=[
                    "master_table",
                    "baseline_model",
                    "baseline_metrics",
                    "optimized_model",
                    "optimized_metrics",
                    "xgboost_model",
                    "xgboost_metrics",
                    "params:columns",
                    "params:decision_policies",
                    "params:model_selection",
                ],
                outputs="model_selection_scorecard",
                name="build_model_selection_scorecard_node",
                tags=["modelling", "selection", "policy"],
            ),
            node(
                func=select_best_model,
                inputs=[
                    "baseline_model",
                    "baseline_metrics",
                    "optimized_model",
                    "optimized_metrics",
                    "xgboost_model",
                    "xgboost_metrics",
                    "params:model_selection",
                    "model_selection_scorecard",
                ],
                outputs="best_model_config_raw",
                name="select_best_model_node",
                tags=["modelling", "selection"],
            ),
            node(
                func=build_feature_selection_bundle,
                inputs=[
                    "best_model_config_raw",
                    "baseline_selected_feature_columns",
                    "baseline_feature_selection_frontier",
                    "baseline_feature_selection_stability",
                    "baseline_feature_selection_manifest",
                    "optimized_selected_feature_columns",
                    "optimized_feature_selection_frontier",
                    "optimized_feature_selection_stability",
                    "optimized_feature_selection_manifest",
                    "xgboost_selected_feature_columns",
                    "xgboost_feature_selection_frontier",
                    "xgboost_feature_selection_stability",
                    "xgboost_feature_selection_manifest",
                ],
                outputs=[
                    "selected_feature_columns",
                    "feature_selection_frontier",
                    "feature_selection_stability",
                    "feature_selection_manifest",
                ],
                name="build_feature_selection_bundle_node",
                tags=["modelling", "feature_selection", "selection"],
            ),
            node(
                func=build_threshold_metrics,
                inputs=[
                    "best_model_config_raw",
                    "baseline_model",
                    "optimized_model",
                    "xgboost_model",
                    "master_table",
                    "params:columns",
                    "params:decision_policies",
                ],
                outputs="threshold_metrics",
                name="build_threshold_metrics_node",
                tags=["modelling", "evaluation", "policy"],
            ),
            node(
                func=select_deployment_policy,
                inputs=[
                    "best_model_config_raw",
                    "threshold_metrics",
                    "params:decision_policies",
                ],
                outputs="selected_deployment_policy",
                name="select_deployment_policy_node",
                tags=["modelling", "selection", "policy"],
            ),
            node(
                func=enrich_best_model_config_with_policy,
                inputs=[
                    "best_model_config_raw",
                    "selected_deployment_policy",
                ],
                outputs="best_model_config",
                name="enrich_best_model_config_node",
                tags=["modelling", "selection", "policy"],
            ),
            node(
                func=build_cv_fold_metrics,
                inputs=[
                    "master_table",
                    "baseline_model",
                    "optimized_model",
                    "xgboost_model",
                    "params:columns",
                    "params:ml_runtime",
                    "params:decision_policies",
                    "selected_deployment_policy",
                ],
                outputs="cv_fold_metrics",
                name="build_cv_fold_metrics_node",
                tags=["modelling", "evaluation", "robustness"],
            ),
            node(
                func=summarize_cv_fold_metrics,
                inputs=["cv_fold_metrics"],
                outputs="cv_metric_summary",
                name="summarize_cv_fold_metrics_node",
                tags=["modelling", "evaluation", "robustness"],
            ),
            node(
                func=build_bootstrap_metric_intervals,
                inputs=[
                    "master_table",
                    "baseline_model",
                    "optimized_model",
                    "xgboost_model",
                    "params:columns",
                    "params:evaluation",
                    "params:decision_policies",
                    "selected_deployment_policy",
                    "params:bootstrap_confidence_intervals",
                ],
                outputs="bootstrap_metric_intervals",
                name="build_bootstrap_metric_intervals_node",
                tags=["modelling", "evaluation", "robustness"],
            ),
            node(
                func=build_model_frontier,
                inputs=[
                    "baseline_model",
                    "baseline_metrics",
                    "optimized_model",
                    "optimized_metrics",
                    "xgboost_model",
                    "xgboost_metrics",
                    "best_model_config",
                    "selected_deployment_policy",
                    "model_selection_scorecard",
                ],
                outputs="model_frontier",
                name="build_model_frontier_node",
                tags=["modelling", "evaluation", "reporting"],
            ),
            node(
                func=build_split_comparison_report,
                inputs=[
                    "best_model_config_raw",
                    "baseline_model",
                    "optimized_model",
                    "xgboost_model",
                    "master_table",
                    "params:columns",
                    "params:decision_policies",
                    "selected_deployment_policy",
                    "params:model_selection",
                ],
                outputs="split_comparison_report",
                name="build_split_comparison_report_node",
                tags=["modelling", "evaluation", "reporting"],
            ),
            node(
                func=build_permutation_feature_importance,
                inputs=[
                    "best_model_config",
                    "baseline_model",
                    "optimized_model",
                    "xgboost_model",
                    "master_table",
                    "params:columns",
                    "params:decision_policies",
                    "selected_deployment_policy",
                    "params:permutation_importance",
                ],
                outputs="permutation_feature_importance",
                name="build_permutation_feature_importance_node",
                tags=["modelling", "evaluation", "robustness"],
            ),
            node(
                func=build_perturbation_sensitivity_audit,
                inputs=[
                    "best_model_config",
                    "baseline_model",
                    "optimized_model",
                    "xgboost_model",
                    "master_table",
                    "params:columns",
                    "selected_deployment_policy",
                    "params:sensitivity_audit",
                ],
                outputs="perturbation_sensitivity_audit",
                name="build_perturbation_sensitivity_audit_node",
                tags=["modelling", "evaluation", "robustness"],
            ),
            node(
                func=summarize_perturbation_sensitivity_audit,
                inputs=["perturbation_sensitivity_audit"],
                outputs="perturbation_sensitivity_summary",
                name="summarize_perturbation_sensitivity_audit_node",
                tags=["modelling", "evaluation", "robustness"],
            ),
            node(
                func=build_nested_cv_audit,
                inputs=[
                    "master_table",
                    "params:columns",
                    "params:ml_runtime",
                    "params:feature_selection",
                    "params:baseline",
                    "params:optimization",
                    "params:xgboost",
                    "params:evaluation",
                    "params:model_selection",
                    "params:decision_policies",
                    "params:nested_cv_audit",
                    "params:calibration",
                ],
                outputs="nested_cv_audit",
                name="build_nested_cv_audit_node",
                tags=["modelling", "evaluation", "robustness"],
            ),
            node(
                func=build_modelling_design_audit,
                inputs=[
                    "params:raw_columns",
                    "params:columns",
                    "selected_feature_columns",
                    "feature_selection_manifest",
                    "params:model_selection",
                    "params:decision_policies",
                    "perturbation_sensitivity_summary",
                ],
                outputs="modelling_design_audit",
                name="build_modelling_design_audit_node",
                tags=["modelling", "governance", "robustness"],
            ),
            node(
                func=record_experiment_run,
                inputs=[
                    "model_frontier",
                    "threshold_metrics",
                    "cv_metric_summary",
                    "selected_deployment_policy",
                    "best_model_config",
                    "params:model_selection",
                ],
                outputs="latest_experiment_run",
                name="record_experiment_run_node",
                tags=["modelling", "registry"],
            ),
            node(
                func=build_training_run_manifest,
                inputs=[
                    "latest_experiment_run",
                    "bootstrap_metric_intervals",
                    "permutation_feature_importance",
                    "perturbation_sensitivity_summary",
                    "split_strategy_report",
                    "feature_selection_frontier",
                    "feature_selection_stability",
                    "feature_selection_manifest",
                    "model_selection_scorecard",
                    "split_comparison_report",
                    "nested_cv_audit",
                    "modelling_design_audit",
                ],
                outputs="latest_training_run_manifest",
                name="build_training_run_manifest_node",
                tags=["modelling", "registry", "governance"],
            ),
            node(
                func=evaluate_all_on_test,
                inputs=[
                    "master_table",
                    "baseline_model",
                    "optimized_model",
                    "xgboost_model",
                    "params:columns",
                    "params:evaluation",
                ],
                outputs="test_report",
                name="test_evaluation_node",
                tags=["modelling", "evaluation"],
            ),
        ]
    )
